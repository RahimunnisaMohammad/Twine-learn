package com.example.demo.model;
public enum Role {
    STUDENT("Student"),
    MENTOR("Mentor");

    private final String displayName;

    Role(String displayName) {
        this.displayName = displayName;
    }

    public String getDisplayName() {
        return displayName;
    }

    @Override
    public String toString() {
        return displayName;
    }
}
